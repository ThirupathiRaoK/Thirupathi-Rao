#include<stdio.h>
main()
{
	int n;
	printf("\n enter a day no :");
	scanf("%d",&n);
	switch (n)
	{
		case1:printf("\n sunday");break;
		case2:printf("\n monday");break;
		case3:printf("\n tuesday");break;
		case4:printf("\n wednesday");break;
		case5:printf("\n thursday");break;
		case6:printf("\n friday");break;
		case7:printf("\n saturday");break;
	}
}
